# Задача 4: Словарь квадратов
#
# Условие: Дано целое число n. Создайте словарь, где
# ключами будут числа от 1 до n, а значениями — их
# квадраты.
#
# n = 5
# Вывод: {1: 1, 2: 4, 3: 9, 4: 16, 5: 25}

n = 5
dictionary = {}

i = 1

for i in range(i, n + 1):
    dictionary[i] = i ** 2

print(dictionary)
